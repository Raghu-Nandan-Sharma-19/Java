public class RelationalOperators {
    public static void main(String[] args) {
        int a = 7;
        int b = 3;

        // is equals to
        System.out.println(a == b);

        // not equals to
        System.out.println(a != b);

        // greater than
        System.out.println(a > b);

        // less than
        System.out.println(a < b);

        // greater than or equals to
        System.out.println(a >= b);

        // less than or equals to
        System.out.println(a <= b);
    }
}