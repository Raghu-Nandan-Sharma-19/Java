public class IfElse {
    public static void main(String[] args) {
        int age = 24;
        if(age >= 18) {
            System.out.println("You can vote");
        } else {
            System.out.println("You can not vote");
        }
        int day = 1;
        if(day == 1) {
            System.out.println("Go to Home");
        } else {
            System.out.println("Go to college");
        }
    }
}