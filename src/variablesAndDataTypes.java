public class variablesAndDataTypes {
    public static void main(String[] args) {
        String name = "Raghu Nandan Sharma"; // Data type : String
        int age = 19; // Integer
        double price = 25.25;  // double floating point
        int length = 25;  // integer
        int width = 10;   // integer
        System.out.println("Name is : "+ name);
        System.out.println("Age is : "+ age);
        System.out.println("Price of pen is : "+ price);
        System.out.println("Length of rectangle is : "+ length);
        System.out.println("Width of rectangle is : "+ width);
    }
}
