public class ElseIf {
    public static void main(String[] args) {
        int day = 2;
        if(day == 1) {
            System.out.println("Go to Home");
        } else if(day == 2) {
            System.out.println("Go to village");
        } else if(day == 3) {
            System.out.println("Go to party");
        } else {
            System.out.println("Go to office");
        }
    }
}