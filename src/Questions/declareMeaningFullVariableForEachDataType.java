package Questions;

public class declareMeaningFullVariableForEachDataType {
    public static void main(String[] args) {
        byte number1 = 1;
        short number2 = 32;
        int number3 = 9999991;
        long number4 = 682492492;
        float floatingPointNumber = 24.9898532F;
        double doubleFloatingPointNumber = 24.98743161864311;
        char alphabet = 'a';
        boolean check = true;
        System.out.println("Byte : "+number1);
        System.out.println("Short : "+number2);
        System.out.println("Integer : "+number3);
        System.out.println("Long : "+number4);
        System.out.println("float : "+floatingPointNumber);
        System.out.println("double : "+doubleFloatingPointNumber);
        System.out.println("character : "+alphabet);
        System.out.println("boolean(true or false) : "+check);
    }
}
