public class ArithmeticOperators {
    public static void main(String[] args) {
        // Arithmetic operators
        int a = 7;
        int b = 3;
        // addition
        System.out.println(a + b);
        // subtraction
        System.out.println(a - b);
        // multiplication
        System.out.println(a * b);
        // division
        System.out.println((double)a / b);
        // modulo operation (remainder after division)
        System.out.println(a % b);
    }
}
