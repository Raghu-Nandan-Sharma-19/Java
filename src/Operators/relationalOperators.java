package Operators;

public class relationalOperators {
    public static void main(String[] args) {
        int a = 10, b = 5;
        // equals to
        System.out.println(a==b); // false
        // not equals to
        System.out.println(a!=b); // true
        // greater than
        System.out.println(a>b); // true
        // less than
        System.out.println(a<b); // false
        //  greater than equal to
        System.out.println(a>=b); // true
        // less than equal to
        System.out.println(a<=b); // false
    }
}
